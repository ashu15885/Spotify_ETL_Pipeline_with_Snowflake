-- Automated Spotify pipeline using append-only Streams with MERGE-based CDC and audit logging
-- Co-authored with CoCo
USE SPOTIFY_DB;

USE SCHEMA RAW_LAYER;

-- ================================================
-- STREAMS - Recreate as append-only (flushes stale data, starts fresh)
-- ================================================

CREATE OR REPLACE STREAM STREAM_RAW_SONGS
    ON TABLE RAW_LAYER.RAW_SONGS
    APPEND_ONLY = TRUE;

CREATE OR REPLACE STREAM STREAM_RAW_ARTISTS
    ON TABLE RAW_LAYER.RAW_ARTISTS
    APPEND_ONLY = TRUE;

CREATE OR REPLACE STREAM STREAM_RAW_ALBUMS
    ON TABLE RAW_LAYER.RAW_ALBUMS
    APPEND_ONLY = TRUE;

-- ================================================
-- TASKS - Orchestrate the full pipeline
-- ================================================

-- Cleanup: Suspend and drop existing tasks before recreating (children first, then root)
ALTER TASK IF EXISTS TASK_LOAD_FACTS SUSPEND;
ALTER TASK IF EXISTS TASK_LOAD_DIMENSIONS_ALBUMS SUSPEND;
ALTER TASK IF EXISTS TASK_LOAD_DIMENSIONS_ARTISTS SUSPEND;
ALTER TASK IF EXISTS TASK_LOAD_STAGING_SONGS SUSPEND;
ALTER TASK IF EXISTS TASK_LOAD_STAGING_ARTISTS SUSPEND;
ALTER TASK IF EXISTS TASK_LOAD_STAGING_ALBUMS SUSPEND;
ALTER TASK IF EXISTS TASK_ROOT_SPOTIFY_PIPELINE SUSPEND;

DROP TASK IF EXISTS TASK_LOAD_FACTS;
DROP TASK IF EXISTS TASK_LOAD_DIMENSIONS_ALBUMS;
DROP TASK IF EXISTS TASK_LOAD_DIMENSIONS_ARTISTS;
DROP TASK IF EXISTS TASK_LOAD_STAGING_SONGS;
DROP TASK IF EXISTS TASK_LOAD_STAGING_ARTISTS;
DROP TASK IF EXISTS TASK_LOAD_STAGING_ALBUMS;
DROP TASK IF EXISTS TASK_ROOT_SPOTIFY_PIPELINE;

-- ================================================
-- Root Task: Runs every 5 minutes (Europe/Berlin)
-- ================================================
CREATE OR REPLACE TASK TASK_ROOT_SPOTIFY_PIPELINE
    WAREHOUSE = SPOTIFY_ETL_WH
    SCHEDULE  = 'USING CRON */5 * * * * Europe/Berlin'
AS
    SELECT 'Pipeline triggered at: ' || CURRENT_TIMESTAMP();

-- ================================================
-- Task: MERGE Songs into Staging from Stream (with audit)
-- ================================================
CREATE OR REPLACE TASK TASK_LOAD_STAGING_SONGS
    WAREHOUSE = SPOTIFY_ETL_WH
    AFTER TASK_ROOT_SPOTIFY_PIPELINE
WHEN
    SYSTEM$STREAM_HAS_DATA('RAW_LAYER.STREAM_RAW_SONGS')
AS
BEGIN
    LET v_start TIMESTAMP_NTZ := CURRENT_TIMESTAMP();
    LET v_run_id VARCHAR := TO_VARCHAR(CURRENT_TIMESTAMP(), 'YYYYMMDDHH24MISS');
    LET v_rows_merged NUMBER := 0;
    LET v_rows_deleted NUMBER := 0;

    BEGIN TRANSACTION;

    -- MERGE inserts and updates from stream into staging
    MERGE INTO STAGING_LAYER.STG_SONGS AS tgt
    USING (
        SELECT
            TRIM(SONG_ID) AS SONG_ID,
            TRIM(SONG_NAME) AS SONG_NAME,
            TRY_TO_NUMBER(SONG_DURATION) AS SONG_DURATION_MS,
            FLOOR(TRY_TO_NUMBER(SONG_DURATION) / 60000) AS DURATION_MINUTES,
            ROUND((TRY_TO_NUMBER(SONG_DURATION)/1000) - 
                  (FLOOR(TRY_TO_NUMBER(SONG_DURATION)/60000)*60)) AS DURATION_SECONDS,
            TRIM(SONG_URL) AS SONG_URL,
            TRY_TO_TIMESTAMP(SONG_ADDED) AS SONG_ADDED_TIMESTAMP,
            DATE(TRY_TO_TIMESTAMP(SONG_ADDED)) AS SONG_ADDED_DATE,
            TRIM(ALBUM_ID) AS ALBUM_ID,
            TRIM(ARTIST_ID) AS ARTIST_ID,
            CASE
                WHEN SONG_ID IS NULL                       THEN 'MISSING_SONG_ID'
                WHEN TRY_TO_NUMBER(SONG_DURATION) IS NULL  THEN 'INVALID_DURATION'
                ELSE 'VALID'
            END AS DATA_QUALITY_FLAG,
            _LOAD_TIMESTAMP
        FROM RAW_LAYER.STREAM_RAW_SONGS
    ) AS src
    ON tgt.SONG_ID = src.SONG_ID
    WHEN MATCHED THEN UPDATE SET
        tgt.SONG_NAME = src.SONG_NAME,
        tgt.SONG_DURATION_MS = src.SONG_DURATION_MS,
        tgt.DURATION_MINUTES = src.DURATION_MINUTES,
        tgt.DURATION_SECONDS = src.DURATION_SECONDS,
        tgt.SONG_URL = src.SONG_URL,
        tgt.SONG_ADDED_TIMESTAMP = src.SONG_ADDED_TIMESTAMP,
        tgt.SONG_ADDED_DATE = src.SONG_ADDED_DATE,
        tgt.ALBUM_ID = src.ALBUM_ID,
        tgt.ARTIST_ID = src.ARTIST_ID,
        tgt.DATA_QUALITY_FLAG = src.DATA_QUALITY_FLAG,
        tgt._LOAD_TIMESTAMP = src._LOAD_TIMESTAMP,
        tgt._TRANSFORM_TIMESTAMP = CURRENT_TIMESTAMP()
    WHEN NOT MATCHED THEN INSERT (
        SONG_ID, SONG_NAME, SONG_DURATION_MS, DURATION_MINUTES, DURATION_SECONDS,
        SONG_URL, SONG_ADDED_TIMESTAMP, SONG_ADDED_DATE,
        ALBUM_ID, ARTIST_ID, DATA_QUALITY_FLAG,
        _LOAD_TIMESTAMP, _TRANSFORM_TIMESTAMP
    ) VALUES (
        src.SONG_ID, src.SONG_NAME, src.SONG_DURATION_MS, src.DURATION_MINUTES, src.DURATION_SECONDS,
        src.SONG_URL, src.SONG_ADDED_TIMESTAMP, src.SONG_ADDED_DATE,
        src.ALBUM_ID, src.ARTIST_ID, src.DATA_QUALITY_FLAG,
        src._LOAD_TIMESTAMP, CURRENT_TIMESTAMP()
    );
    v_rows_merged := SQLROWCOUNT;

    -- Delete stale records: guard + lookup raw with NEW status
    DELETE FROM STAGING_LAYER.STG_SONGS
    WHERE (SELECT COUNT(*) FROM RAW_LAYER.RAW_SONGS WHERE LOAD_STATUS = 'NEW') > 0
    AND SONG_ID NOT IN (
        SELECT TRIM(SONG_ID) FROM RAW_LAYER.RAW_SONGS WHERE LOAD_STATUS = 'NEW'
    );
    v_rows_deleted := SQLROWCOUNT;

    -- Mark consumed records as LOADED
    UPDATE RAW_LAYER.RAW_SONGS
    SET LOAD_STATUS = 'LOADED'
    WHERE LOAD_STATUS = 'NEW';

    -- Audit log
    INSERT INTO RAW_LAYER.ETL_AUDIT_LOG (PIPELINE_RUN_ID, TASK_NAME, TASK_LAYER, OPERATION, TARGET_TABLE, ROWS_INSERTED, ROWS_UPDATED, ROWS_DELETED, STATUS, START_TIME, END_TIME, DURATION_SECONDS)
    SELECT :v_run_id, 'TASK_LOAD_STAGING_SONGS', 'STAGING', 'MERGE+DELETE', 'STG_SONGS', :v_rows_merged, 0, :v_rows_deleted, 'SUCCESS', :v_start, CURRENT_TIMESTAMP(), DATEDIFF('second', :v_start, CURRENT_TIMESTAMP());

    COMMIT;
EXCEPTION
    WHEN OTHER THEN
        ROLLBACK;
        RAISE;
END;

