-- ================================================
-- Task: Load Dimensions - Artists (SCD Type 2 with audit)
-- ================================================
CREATE OR REPLACE TASK TASK_LOAD_DIMENSIONS_ARTISTS
    WAREHOUSE = SPOTIFY_ETL_WH
    AFTER TASK_LOAD_STAGING_ARTISTS
AS
BEGIN
    LET v_start TIMESTAMP_NTZ := CURRENT_TIMESTAMP();
    LET v_run_id VARCHAR := TO_VARCHAR(CURRENT_TIMESTAMP(), 'YYYYMMDDHH24MISS');
    LET v_rows_closed NUMBER := 0;
    LET v_rows_inserted NUMBER := 0;

    BEGIN TRANSACTION;

    -- MERGE: Close records that are changed or deleted
    MERGE INTO WAREHOUSE_LAYER.DIM_ARTISTS AS tgt
    USING (
        SELECT ARTIST_ID, ARTIST_NAME, EXTERNAL_URL, 'UPSERT' AS ACTION
        FROM STAGING_LAYER.STG_ARTISTS
        WHERE DATA_QUALITY_FLAG = 'VALID'
        UNION ALL
        SELECT d.ARTIST_ID, NULL, NULL, 'DELETE'
        FROM WAREHOUSE_LAYER.DIM_ARTISTS d
        WHERE d.IS_CURRENT = TRUE
        AND d.ARTIST_ID NOT IN (SELECT ARTIST_ID FROM STAGING_LAYER.STG_ARTISTS)
    ) AS src
    ON tgt.ARTIST_ID = src.ARTIST_ID AND tgt.IS_CURRENT = TRUE
    WHEN MATCHED AND src.ACTION = 'DELETE' THEN
        UPDATE SET tgt.EFF_END_DATE = CURRENT_DATE() - 1, tgt.IS_CURRENT = FALSE, tgt.UPDATED_AT = CURRENT_TIMESTAMP()
    WHEN MATCHED AND src.ACTION = 'UPSERT'
        AND (tgt.ARTIST_NAME != src.ARTIST_NAME OR tgt.EXTERNAL_URL != src.EXTERNAL_URL) THEN
        UPDATE SET tgt.EFF_END_DATE = CURRENT_DATE() - 1, tgt.IS_CURRENT = FALSE, tgt.UPDATED_AT = CURRENT_TIMESTAMP()
    WHEN NOT MATCHED AND src.ACTION = 'UPSERT' THEN
        INSERT (ARTIST_ID, ARTIST_NAME, EXTERNAL_URL, EFF_START_DATE, EFF_END_DATE, IS_CURRENT)
        VALUES (src.ARTIST_ID, src.ARTIST_NAME, src.EXTERNAL_URL, CURRENT_DATE(), NULL, TRUE);
    v_rows_closed := SQLROWCOUNT;

    -- INSERT: new current version for closed records
    INSERT INTO WAREHOUSE_LAYER.DIM_ARTISTS (
        ARTIST_ID, ARTIST_NAME, EXTERNAL_URL,
        EFF_START_DATE, EFF_END_DATE, IS_CURRENT
    )
    SELECT ARTIST_ID, ARTIST_NAME, EXTERNAL_URL, CURRENT_DATE(), NULL, TRUE
    FROM STAGING_LAYER.STG_ARTISTS
    WHERE DATA_QUALITY_FLAG = 'VALID'
    AND ARTIST_ID NOT IN (
        SELECT ARTIST_ID FROM WAREHOUSE_LAYER.DIM_ARTISTS WHERE IS_CURRENT = TRUE
    );
    v_rows_inserted := SQLROWCOUNT;

    INSERT INTO RAW_LAYER.ETL_AUDIT_LOG (PIPELINE_RUN_ID, TASK_NAME, TASK_LAYER, OPERATION, TARGET_TABLE, ROWS_INSERTED, ROWS_UPDATED, ROWS_DELETED, STATUS, START_TIME, END_TIME, DURATION_SECONDS)
    SELECT :v_run_id, 'TASK_LOAD_DIMENSIONS_ARTISTS', 'WAREHOUSE', 'SCD2_MERGE+INSERT', 'DIM_ARTISTS', :v_rows_inserted, :v_rows_closed, 0, 'SUCCESS', :v_start, CURRENT_TIMESTAMP(), DATEDIFF('second', :v_start, CURRENT_TIMESTAMP());

    COMMIT;
EXCEPTION
    WHEN OTHER THEN
        ROLLBACK;
        RAISE;
END;

