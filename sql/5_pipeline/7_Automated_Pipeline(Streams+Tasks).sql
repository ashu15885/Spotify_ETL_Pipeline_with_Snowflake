-- ================================================
-- Task: Load Fact Table using MERGE (with audit)
-- ================================================
CREATE OR REPLACE TASK TASK_LOAD_FACTS
    WAREHOUSE = SPOTIFY_ETL_WH
    AFTER TASK_LOAD_DIMENSIONS_ARTISTS, TASK_LOAD_DIMENSIONS_ALBUMS
AS
BEGIN
    LET v_start TIMESTAMP_NTZ := CURRENT_TIMESTAMP();
    LET v_run_id VARCHAR := TO_VARCHAR(CURRENT_TIMESTAMP(), 'YYYYMMDDHH24MISS');
    LET v_rows_merged NUMBER := 0;
    LET v_rows_invalidated NUMBER := 0;
    LET v_rows_revalidated NUMBER := 0;

    BEGIN TRANSACTION;

    MERGE INTO WAREHOUSE_LAYER.FACT_SONGS AS tgt
    USING (
        SELECT
            s.SONG_ID,
            a.ARTIST_SK,
            al.ALBUM_SK,
            s.SONG_NAME,
            s.SONG_DURATION_MS,
            s.DURATION_MINUTES,
            s.DURATION_SECONDS,
            s.SONG_URL,
            s.SONG_ADDED_TIMESTAMP,
            s.SONG_ADDED_DATE
        FROM STAGING_LAYER.STG_SONGS s
        LEFT JOIN WAREHOUSE_LAYER.DIM_ARTISTS a
            ON s.ARTIST_ID = a.ARTIST_ID AND a.IS_CURRENT = TRUE
        LEFT JOIN WAREHOUSE_LAYER.DIM_ALBUMS al
            ON s.ALBUM_ID = al.ALBUM_ID AND al.IS_CURRENT = TRUE
        WHERE s.DATA_QUALITY_FLAG = 'VALID'
    ) AS src
    ON tgt.SONG_ID = src.SONG_ID
    WHEN MATCHED AND (
        tgt.ARTIST_SK != src.ARTIST_SK
        OR tgt.ALBUM_SK != src.ALBUM_SK
        OR tgt.SONG_NAME != src.SONG_NAME
        OR tgt.SONG_DURATION_MS != src.SONG_DURATION_MS
        OR tgt.SONG_URL != src.SONG_URL
        OR tgt.SONG_ADDED_TIMESTAMP != src.SONG_ADDED_TIMESTAMP
    ) THEN UPDATE SET
        tgt.ARTIST_SK = src.ARTIST_SK,
        tgt.ALBUM_SK = src.ALBUM_SK,
        tgt.SONG_NAME = src.SONG_NAME,
        tgt.SONG_DURATION_MS = src.SONG_DURATION_MS,
        tgt.DURATION_MINUTES = src.DURATION_MINUTES,
        tgt.DURATION_SECONDS = src.DURATION_SECONDS,
        tgt.SONG_URL = src.SONG_URL,
        tgt.SONG_ADDED_TIMESTAMP = src.SONG_ADDED_TIMESTAMP,
        tgt.SONG_ADDED_DATE = src.SONG_ADDED_DATE
    WHEN NOT MATCHED THEN INSERT (
        SONG_ID, ARTIST_SK, ALBUM_SK, SONG_NAME,
        SONG_DURATION_MS, DURATION_MINUTES, DURATION_SECONDS,
        SONG_URL, SONG_ADDED_TIMESTAMP, SONG_ADDED_DATE
    ) VALUES (
        src.SONG_ID, src.ARTIST_SK, src.ALBUM_SK, src.SONG_NAME,
        src.SONG_DURATION_MS, src.DURATION_MINUTES, src.DURATION_SECONDS,
        src.SONG_URL, src.SONG_ADDED_TIMESTAMP, src.SONG_ADDED_DATE
    );
    v_rows_merged := SQLROWCOUNT;

    -- Soft delete: mark songs no longer in staging as invalid
    UPDATE WAREHOUSE_LAYER.FACT_SONGS
    SET IS_VALID = FALSE
    WHERE SONG_ID NOT IN (SELECT SONG_ID FROM STAGING_LAYER.STG_SONGS)
    AND IS_VALID = TRUE;
    v_rows_invalidated := SQLROWCOUNT;

    -- Re-validate songs that reappear in staging
    UPDATE WAREHOUSE_LAYER.FACT_SONGS
    SET IS_VALID = TRUE
    WHERE SONG_ID IN (SELECT SONG_ID FROM STAGING_LAYER.STG_SONGS WHERE DATA_QUALITY_FLAG = 'VALID')
    AND IS_VALID = FALSE;
    v_rows_revalidated := SQLROWCOUNT;

